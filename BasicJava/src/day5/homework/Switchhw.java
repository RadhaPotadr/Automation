public class Switchhw {
	public static void main(String[] args) {
		int i=9,j=3;
		int a=1;
		switch (a) {
		case 1:
		System.out.println(i+j);
		break;
		case 2:
		System.out.println(i-j);
		break;
		case 3:
		System.out.println(i*j);
		break;
		case 4:
		System.out.println(i/j);
		break;
		default:
			System.out.println("invalid");
		}

	}

}
