package day10.pyramid;
public class Full {
	public static void main(String[] args) {
		int i =0,j=0;
       for(i=0;i<5;i++) {
    	   for(j=0;j<=i;j++) {
    		   System.out.print ("* ");
    	   }System.out.println();
       }
       System.out.println();
       int p =0,q=0;
       for(p=5;p>0;p--) {
    	   for(q=0;q<p;q++) {
    		   System.out.print ("* ");
    	   }System.out.println();
       }
       System.out.println();
       int a =0,b=0;
       for(a=5;a>0;a--) {
    	   for(b=0;b<a;b++) {
    		   System.out.print (a+" ");
    	   }System.out.println();
       }
       System.out.println();
       int l =0,m=0;
       for(l=1;l<=5;l++) {
    	   for(m=0;m<l;m++) {
    		   System.out.print (l+" ");
    	   }System.out.println();
       }
       System.out.println();
       char c1='a';
       char c2='a';
       for(c1='a';c1<='e';c1++) {
    	   for(c2='a';c2<=c1;c2++) {
    		   System.out.print (c1+" ");
    	   }System.out.println();
       }
       System.out.println();
       char c3='a';
       char c4='a';
       for(c3='e';c3>='a';c3--) {
    	   for(c4='a';c4<=c3;c4++) {
    		   System.out.print (c3+" ");
    	   }System.out.println();
	}
       System.out.println();
       char c5='a';
       char c6='a';
       for(c5='a';c5<='e';c5++) {
    	   for(c6='a';c6<=c5;c6++) {
    		   System.out.print (c6+" ");
    	   }System.out.println();
       }
       System.out.println();
       char c7='a';
       char c8='a';
       for(c7='e';c7>='a';c7--) {
    	   for(c8='a';c8<=c7;c8++) {
    		   System.out.print (c8+" ");
    	   }System.out.println();
	}
      System.out.println();
       int u=0,v=0;
       for(u=0;u<=5;u++) {
    	   for(v=1;v<=u;v++) {
    		   System.out.print (v+" ");
    	   }System.out.println();
       }
       System.out.println();
       int x=0,y=0;
       for(x=0;x<5;x++) {
    	   for(y=5;y>x;y--) {
    		   System.out.print (y+" ");
    	   }System.out.println();
	}}}
